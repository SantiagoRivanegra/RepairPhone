    public InformacionVenta() {
        initComponents();
        user = Login.user;
        cliente = VentasRealizadas.clienteVenta;

        setSize(550, 430);
        setLocationRelativeTo(null);
        setResizable(false);
        setTitle("Informacion Venta - Sesion de " + user);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        ImageIcon wallpaper = new ImageIcon("src/images/wallpaper.png");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(), jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select nombre, dni, cuit, direccion, telefono, mail, tipo, marca, modelo, precio from ventas"
                    + " where nombre = '" + cliente + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
            txt_nombreCliente.setText(rs.getString("nombre"));
            txt_nombreCliente.setEnabled(false);
            txt_DNI.setText(rs.getString("dni"));
            txt_DNI.setEnabled(false);
            txt_CUIT.setText(rs.getString("cuit"));
            txt_CUIT.setEnabled(false);
            txt_direccion.setText(rs.getString("direccion"));
            txt_direccion.setEnabled(false);
            txt_telefono.setText(rs.getString("telefono"));
            txt_telefono.setEnabled(false);
            txt_mail.setText(rs.getString("mail"));
            txt_mail.setEnabled(false);
            txt_tipo.setText(rs.getString("tipo"));
            txt_tipo.setEnabled(false);
            txt_marca.setText(rs.getString("marca"));
            txt_marca.setEnabled(false);
            txt_modelo.setText(rs.getString("modelo"));
            txt_modelo.setEnabled(false);
            txt_precio.setText(rs.getString("precio"));
            txt_precio.setEnabled(false);
                    }
            
        } catch (SQLException e) {
            System.err.println("Error al obtener los datos de venta" + e);
            JOptionPane.showMessageDialog(null,"Error al obtener los datos de venta");
        }

    }

