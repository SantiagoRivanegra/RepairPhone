    private void jButton_actualizarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarUsuarioActionPerformed
        
        int cmb_niveles, cmb_estados, validacion = 0;
        String mail, telefono, nombre, username, pass, estado_string = "", nivel_string = "";
        
        mail = txt_mail.getText().trim();
        nombre = txt_nombreUsuario.getText().trim();
        telefono = txt_telefono.getText().trim();
        username = txt_username.getText().trim();
        cmb_estados = cmb_estado.getSelectedIndex() + 1;
        cmb_niveles = cmb_nivel.getSelectedIndex() + 1;
        
        if (mail.equals("")){
            txt_mail.setBackground(Color.pink);            
            validacion++;
        }        
        if (nombre.equals("")){
            txt_nombreUsuario.setBackground(Color.pink);            
            validacion++;
        }
        if (username.equals("")){
            txt_username.setBackground(Color.pink);            
            validacion++;
        }
        if (telefono.equals("")){
            txt_telefono.setBackground(Color.pink);
            validacion++;
        }
        
        if (validacion == 0){
            
            if (cmb_niveles == 1){
                nivel_string = "Administrador";
            } else if (cmb_niveles == 2){
                nivel_string = "Empleado";
            }
            
            if (cmb_estados == 1){
                estado_string = "Activo";
            } else if (cmb_estados == 2){
                estado_string = "Inactivo";
            }
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("select username from usuarios where username = '" + username + "' and not id_usuarios = '" + ID + "'");
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()){
                    txt_username.setBackground(Color.pink);
                    JOptionPane.showMessageDialog(null,"Nombre de usuario no disponible");
                    cn.close();
                } else {
                    Connection cn2 = Conexion.conectar();
                    PreparedStatement pst2 = cn2.prepareStatement("update usuarios set nombre_usuario=?, mail=?, telefono=?, username=?, nivel=?, estado=?"
                            + "where id_usuarios = '" + ID + "'");
                    
                    
                    pst2.setString(1, nombre);
                    pst2.setString(2, mail);
                    pst2.setString(3, telefono);
                    pst2.setString(4, username);
                    pst2.setString(5, nivel_string);
                    pst2.setString(6, estado_string);
                    
                    pst2.executeUpdate();
                    
                    cn2.close();
                    JOptionPane.showMessageDialog(null, "Modificacion correcta");
                }
                
                
            } catch (SQLException e) {
                System.err.println("Error en actualizar informacion" + e);
                
            }
        } else{
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
        }
    }//GEN-LAST:event_jButton_actualizarUsuarioActionPerformed

