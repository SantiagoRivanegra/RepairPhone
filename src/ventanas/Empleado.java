    public Empleado() {
        initComponents();
        user = Login.user;
        
        setTitle("Empleado - Sesion de " + user);
        setSize(600,400);
        setLocationRelativeTo(null);
        setResizable(false);        
        
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaper.png");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),jLabel_Wallpaper.getHeight(),Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select nombre_usuario from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                nombreUsuario = rs.getString("nombre_usuario");
                jLabel_user.setText(nombreUsuario);                
            }
            
        } catch (SQLException e) {
            System.err.println("Error en interfaz Empleado");
        }
        
    }
    
