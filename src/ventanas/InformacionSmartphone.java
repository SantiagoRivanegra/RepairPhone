    private void jButton_actualizarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarEquipoActionPerformed
        String precio, cantidad;

        cantidad = txt_cantidad.getText().trim();
        precio = txt_precio.getText().trim();

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select precio, cantidad from stock where modelo ='" + modelo_smart + "' and "
                    + "marca ='" + marca_smart + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                try {
                    Connection cn2 = Conexion.conectar();
                    PreparedStatement pst2 = cn2.prepareStatement("update stock set precio=?, cantidad=? where modelo ='" + modelo_smart + "' and "
                            + "marca ='" + marca_smart + "'");

                    pst2.setString(1, precio);
                    pst2.setString(2, cantidad);

                    pst2.executeUpdate();
                    cn2.close();
                    JOptionPane.showMessageDialog(null,"Actualizacion exitosa");

                } catch (SQLException e) {
                    System.err.println("Error en actualizar informacion" + e);
                    JOptionPane.showMessageDialog(null,"Error al actualizar informacion");
                }
            }

            cn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Error en conexion a base de datos");
        }
    }//GEN-LAST:event_jButton_actualizarEquipoActionPerformed

