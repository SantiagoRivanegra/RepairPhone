    private void jButton_registrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrarUsuarioActionPerformed
        int cmb_tipos, validar = 0;
        String marca, modelo, precio, tipo_string = "", cantidad;

        marca = txt_marca.getText().trim();
        modelo = txt_modelo.getText().trim();
        precio = txt_precio.getText().trim();
        cantidad = txt_cantidad.getText().trim();
        cmb_tipos = cmb_tipo.getSelectedIndex() + 1;

        if (marca.equals("")) {
            txt_marca.setBackground(Color.pink);
            validar++;
        }
        if (modelo.equals("")) {
            txt_modelo.setBackground(Color.pink);
            validar++;
        }
        if (precio.equals("")) {
            txt_precio.setBackground(Color.pink);
            validar++;
        }
        if (cantidad.equals("")) {
            txt_cantidad.setBackground(Color.pink);
            validar++;
        }
        if (cmb_tipos == 1) {
            tipo_string = "Notebook";
        } else if (cmb_tipos == 2) {
            tipo_string = "Smartphone";
        }

        if (validar == 0){
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("insert into stock values (?,?,?,?,?)");
                
                pst.setString(1, tipo_string);
                pst.setString(2, marca);
                pst.setString(3, modelo);
                pst.setString(4, cantidad);
                pst.setString(5, precio);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                JOptionPane.showMessageDialog(null,"Registro exitoso");
                this.dispose();                   
                          
            } catch (SQLException e) {
                System.err.println("Error en registro de equipos" + e);
                JOptionPane.showMessageDialog(null,"Error en registrar equipos");
            }
        } else {
            JOptionPane.showMessageDialog(null,"Debe llenar todos los campos");
        }


    }//GEN-LAST:event_jButton_registrarUsuarioActionPerformed

    public void Limpiar() {
        txt_marca.setText("");
        txt_modelo.setText("");
        txt_precio.setText("");
        txt_cantidad.setText("");
        cmb_tipo.setSelectedIndex(0);
    }

