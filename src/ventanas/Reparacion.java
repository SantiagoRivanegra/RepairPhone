    private void jButton_ingresoReparacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ingresoReparacionActionPerformed
        String nombre, mail,telefono, ingreso, marca, modelo, tipo, descripcion;
        int validar = 0;

        ingreso = txt_ingreso.getText().trim();
        mail = txt_mail.getText().trim();
        nombre = txt_nombreCliente.getText().trim();
        telefono = txt_telefono.getText().trim();
        tipo = txt_tipo.getText().trim();
        marca = txt_marca.getText().trim();
        modelo = txt_modelo.getText().trim();
        descripcion = area_descripcion.getText().trim();
        

        if ((ingreso.equals(""))|| (mail.equals("")) || (nombre.equals("")) || (telefono.equals(""))
            || (marca.equals("")) || (modelo.equals("")) || (descripcion.equals("")) || (tipo.equals(""))) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
            validar++;
        }

        if (validar == 0) {
            try {
                Connection cn2 = Conexion.conectar();
                PreparedStatement pst = cn2.prepareStatement("insert into reparacion values (?,?,?,?,?,?,?,?)");

                pst.setString(1, nombre);
                pst.setString(2, mail);
                pst.setString(3, telefono);
                pst.setString(4, ingreso);
                pst.setString(5, tipo);
                pst.setString(6, marca);
                pst.setString(7, modelo);
                pst.setString(8, descripcion);

                pst.executeUpdate();
                cn2.close();

                JOptionPane.showMessageDialog(null, "Reparacion para " + marca + " " + modelo + " registrada con exito");
                this.dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al registrar reparacion");
                System.err.println("Error al poner datos en BD" + e);
            }

        }
    }//GEN-LAST:event_jButton_ingresoReparacionActionPerformed

