    private void jButton_confirmaEliminacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_confirmaEliminacionActionPerformed
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("delete from stock where modelo = '" + modelo_note + "' and marca = '" + marca_note + "'");
            
            pst.executeUpdate();
            cn.close();
            
            JOptionPane.showMessageDialog(null,"Notebook " + marca_note + " " + modelo_note + " eliminada del Stock");
        } catch (SQLException e) {
            System.err.println("Error en eliminar notebook" + e);
            JOptionPane.showMessageDialog(null,"Error al eliminar notebook");
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton_confirmaEliminacionActionPerformed

