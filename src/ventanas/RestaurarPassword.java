    private void jButton_restaurarPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_restaurarPasswordActionPerformed
        String passNueva, passConfirma;

        passNueva = txt_passwordNueva.getText().trim();
        passConfirma = txt_passwordConfirma.getText().trim();

        if (!passNueva.equals("") && !passConfirma.equals("")) {
            if (passNueva.equals(passConfirma)) {
                try {
                    Connection cn = Conexion.conectar();
                    PreparedStatement pst = cn.prepareStatement("update usuarios set password=? where username = '" + user_update + "'");

                    pst.setString(1, passNueva);
                    pst.executeUpdate();
                    cn.close();

                    JOptionPane.showMessageDialog(null, "Restauracion exitosa");
                    this.dispose();

                } catch (SQLException e) {
                    System.err.println("Error en restaurar password" + e);
                    JOptionPane.showMessageDialog(null, "Ingres contraseña nuevamente");
                }
            } else {
                txt_passwordConfirma.setBackground(Color.pink);
                JOptionPane.showMessageDialog(null, "Las contraseñas no coincidden");
            }
        } else {
            txt_passwordNueva.setBackground(Color.pink);
            txt_passwordConfirma.setBackground(Color.pink);

            JOptionPane.showMessageDialog(null, "Ingrese contraseña valida. (No se admintes espacios vacios)");
        }       
    }//GEN-LAST:event_jButton_restaurarPasswordActionPerformed

